    private void jDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCab.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblCab.getModel();
        CabProfile selectedCar = (CabProfile) model.getValueAt(selectedRowIndex, 0);

        history.deleteCar(selectedCar);

        JOptionPane.showMessageDialog(this, "Car Details Deleted.");

        populateTable();
        
    }//GEN-LAST:event_jDeleteButtonActionPerformed

    private void jUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdateButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCab.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to Update.");
            return;
        }

        paneView.setVisible(false);
        paneUpdate.setVisible(true);

        DefaultTableModel model = (DefaultTableModel) tblCab.getModel();
        CabProfile selectedCar = (CabProfile) model.getValueAt(selectedRowIndex, 0);
         if (String.valueOf(selectedCar.getOccupied()) == "yes") {
            jCheckBoxOccupiedUpdate.setSelected(true);
            jCheckBoxOccupiedUpdate.setText("");
        } else {
            jCheckBoxOccupiedUpdate.setSelected(false);
            jCheckBoxOccupiedUpdate.setText("");
        }
        jTextRegDate1.setText(String.valueOf(selectedCar.getRegistration_date()));
        jTextFieldBrand1.setText(String.valueOf(selectedCar.getBrand()));
        jTextFieldManufacturingYr1.setText(String.valueOf(selectedCar.getYear()));
        jTextFieldCity1.setText(String.valueOf(selectedCar.getCity()));
        jTextFieldSeatNo1.setText(String.valueOf(selectedCar.getSeats()));
        jTextFieldSerNo1.setText(String.valueOf(selectedCar.getSerial_number()));
        jTextFieldModNo1.setText(String.valueOf(selectedCar.getModel_number()));
        jTextFieldCert1.setText(String.valueOf(selectedCar.getCerti_year()));
    }//GEN-LAST:event_jUpdateButtonActionPerformed

