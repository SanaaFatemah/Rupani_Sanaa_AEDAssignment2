    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblCab.getModel();

        model.setRowCount(0);
//      System.out.println("size"+ history.getHistory().size());

        for (CabProfile CAB : history.getHistory()) {

            Object[] row = new Object[10];
            row[0] = CAB;
            row[1] = CAB.getOccupied();
            row[2] = CAB.getBrand();
            row[3] = CAB.getYear();
            row[4] = CAB.getCity();
            row[5] = CAB.getSeats();
            row[6] = CAB.getSerial_number();
            row[7] = CAB.getModel_number();
            row[8] = CAB.getCerti_year();

            model.addRow(row);

        }
    }

    private void Update(CabProfile ch) {

        Date currentTime = new Date();
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
        String dateTime = dateFormat.format(currentTime);
        ch.setRegistration_date(jTextRegDate1.getText());
         if(this.jCheckBoxOccupiedUpdate.isSelected()){
           ch.setOccupied("yes"); 
        }else{
             ch.setOccupied("no"); 
         }
        ch.setBrand(jTextFieldBrand1.getText());
        ch.setYear(Integer.parseInt(jTextFieldManufacturingYr1.getText()));
        ch.setSerial_number(jTextFieldSerNo1.getText());
        ch.setSeats(Integer.parseInt(jTextFieldSeatNo1.getText()));
        ch.setModel_number(jTextFieldModNo1.getText());
        ch.setCity(jTextFieldCity1.getText());
        ch.setCerti_year(Integer.parseInt(jTextFieldCert1.getText()));
        ch.setTime(dateTime);
        populateTable();

    }
